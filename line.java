public class line {
    int x1;
    int x2;
    int y1;
    int y2;

    public line(int x, int y, int xx, int yy){
        x1 = x;
        x2 = xx;
        y1 = y;
        y2 = yy;
    }

    
    public int getx1(){
        
        return x1;
    }
    public int getx2(){
        
        return x2;
    }
    public int gety1(){
        
        return y1;
    }
    public int gety2(){
        
        return y2;
    }
    
}
